public class BeanDefinitionHolder {

	public String name;
	public BeanDefinition obj;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public BeanDefinition getObj() {
		return obj;
	}
	public void setObj(BeanDefinition obj) {
		this.obj = obj;
	}


}
